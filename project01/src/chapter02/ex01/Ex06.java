package chapter02.ex01;

public class Ex06 {

	public static void main(String[] args) {
		// 3항 연산자  : if ~ else 구문을 축약해서 사용할때 자주 사용하는 구문 
		
			// (조건) ? 참:거짓 ; 		(조건) 참이면 ? 뒤에 <참>을 실행, (조건) 거짓이면 ? <거짓> 실행
		
		int value1 = ( 3 > 5 ) ? 6 : 9 ; 	//(조건) false 이므로 9 가 value1에 들어감 
		System.out.println(value1);	//9
		
		int value2 = ( 3 < 5 ) ? 6 : 9;		//(조건) true 이므로 6가 value2에 들어감 
		System.out.println(value2);	//6
		
		
		//문제 스캐너로 정수값을 인풋받고 인풋받은 정수값이 홀수이면 "홀수" 짝수이면 "짝수" 
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
